    '          domain_context:',
    '            query: "$args.query"',
    '        - op: trigger',
    '          session: "$session.id"',
    '          channel: user_text',
    '          payload: "$args.query"',
    '          mode: async',
    '    abort:',
    '      title: Abort run',

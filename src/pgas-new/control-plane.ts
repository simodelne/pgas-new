    '        - op: abort_session',
    '          session: "$active_session.id"',
    '      presentation:',
    '        priority: destructive',
    '        confirm: true',
    '    new:',
    '      title: New session',
    '      aliases: ["/new", "/reset", "/restart"]',
    '      dispatch:',
    '        - op: create_session',

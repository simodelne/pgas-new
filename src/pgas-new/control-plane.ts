export const PGAS_NEW_CONTROL_PLANE_CONTROLS = [
  'ask',
  'abort',
  'new',
  'history',
  'status',
  'resume',
  'help',
] as const;

export type PgasNewControlPlaneControl = (typeof PGAS_NEW_CONTROL_PLANE_CONTROLS)[number];

export const PGAS_NEW_SESSION_CONTROLS = [
  'new',
  'abort',
  'status',
  'history',
  'resume',
  'help',
] as const satisfies readonly PgasNewControlPlaneControl[];

export type PgasNewSessionControl = (typeof PGAS_NEW_SESSION_CONTROLS)[number];

export function isPgasNewSessionControl(value: string | undefined): value is PgasNewSessionControl {
  return PGAS_NEW_SESSION_CONTROLS.includes(value as PgasNewSessionControl);
}

